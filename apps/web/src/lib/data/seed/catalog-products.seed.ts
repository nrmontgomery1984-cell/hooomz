export async function seedCatalogProductsIfEmpty(services: Services): Promise<void> {
  const sentinel = await services.materialCatalog.findById(SENTINEL_ID);
  if (sentinel) return; // already seeded
  await services.materialCatalog.saveMany(SEED_PRODUCTS);
  console.info(`Seed: ${SEED_PRODUCTS.length} catalog products loaded`);
}
